def generate_error_driven_training_examples(
    *, tool_count: int = 504, dialogue_count: int = 72, seed: int = 29
) -> list[TrainingExample]:
    """Generate the second experiment's balanced error-driven SFT dataset."""

    if tool_count <= 0 or dialogue_count <= 0:
        raise ValueError("tool_count and dialogue_count must be positive")
    rng = random.Random(seed)
    return [
        *(_error_driven_tool_example(index, rng) for index in range(tool_count)),
        *(_dialogue_example(10_000 + index) for index in range(dialogue_count)),
    ]
